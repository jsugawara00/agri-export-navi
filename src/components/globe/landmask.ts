function expand(rle: string): string {
  let out = "";
  for (const m of rle.matchAll(/(\d+)([.#J])/g)) {
    out += m[2].repeat(Number(m[1]));
  }
  if (out.length !== 72) {
    throw new Error(`landmask: 行の長さが72ではありません (${out.length}): ${rle}`);
  }
  return out;
}

const MASK: string[] = MASK_RLE.map(expand);

export type CellType = "sea" | "land" | "japan";

export function sampleMask(lat: number, lng: number): CellType {
  const r = Math.min(35, Math.max(0, Math.floor((90 - lat) / 5)));
  const c = Math.min(71, Math.max(0, Math.floor((lng + 180) / 5)));
  const ch = MASK[r][c];
  return ch === "J" ? "japan" : ch === "#" ? "land" : "sea";
}
